#include <bits/stdc++.h>
using namespace std;
class Solution {
public:
    bool containsDuplicate(vector<int>& nums) {
        unordered_map<int, int> mp;

        for (int i =0; i< nums.size(); i++){
            if(mp.count(nums[i])) return true;
            mp[nums[i]]=i;
        }
        return false;
    }
};